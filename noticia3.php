			Anteriormente conhecida como Mostra Comercial da Grande Pioneiro, a feira é um evento que objetiva divulgar o comércio, a indústria e a prestação de serviços da Grande Pioneiro, bem como fomentar o comércio local.
<br><br>
A FEICOPI teve sua primeira edição realizada no ano de 1995, quando ainda era chamada de Mostra. Ao longo dos anos a Feira se consolidou graças ao esforço de muitas pessoas que se dedicaram e trabalharam para torná-la um evento reconhecido, como é hoje. Desta forma, aproveitamos esta solenidade também para agradecer de maneira especial a todos os coordenadores das edições passadas.
<br>
<br>
 A Feira evoluiu, trouxe novidades e atrações e vem atraindo um público cada vez maior, o que demonstra o potencial das empresas participantes e a credibilidade do evento. A edição deste ano promete superar as expectativas, e está sendo organizada para garantir bons negócios aos expositores e visitantes. 
<br>
<br>
A FEICOPI  é realizada pelo Núcleo Multisetorial da Grande Pioneiro, com o apoio da Associação Comercial e Empresarial de Toledo – ACIT, Sicredi Oeste, Fabiana Supermercados e a Prefeitura do Município de Toledo.
<br><br>
<b>OBJETIVO da FEICOPI:</b><br>
 *  Divulgar e fortalecer o comércio, indústria e Prestação de Serviços da Grande Pioneiro,  buscando  maior representatividade do bairro no município e região. <br><br>
<b>ATRAÇÕES DA FEIRA:</b><br>
*Exposição de empresas de diversos segmentos;<br>
 *Apresentações Artísticas e Culturais;<br>
*Recreação Infantil;<br>
 *Desfile de moda;<br>
*Praça de alimentação.<br>

