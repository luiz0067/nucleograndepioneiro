				   setoriais, para discutir problemas comuns e buscar soluções 
				   conjuntas;<br>
				> Permitir que os empreendedores unam forças, facilitando a  
				   identificação, discussão e busca de soluções para problemas comuns 
				   dos empresários.<br><br>

				Uma vez inseridos nos núcleos setoriais, os empresários de um mesmo 
				ramo de atividade, passam a ser vistos naturalmente como parceiros.
