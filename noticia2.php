								Participaram do Projeto Multi- Setorial 15 Empresas do Núcleo Pioneiro atuantes em vários ramos de atividades, tais como: Confecção e calçados, cosméticos, semi-jóias, refrigeração, imobiliária e comércio alimentício. 
					<br><br>
								<b>Atividades realizadas nas seguintes datas</b><br>
					25/06 a 28/06- Curso Saber Empreender- 16h<br>
					16/07 a 12/11- Consultorias de Finanças- 04h <br>
					02/08 a 20/11- Consultorias de Marketing- 04h<br>
					23/10- Workshop: Gestão do Visual de Loja- 04h <br>
					<br>
					<b>Consultorias de Finanças:</b> Buscou proporcionar aos empreendedores informações financeiras para a tomada de decisão. 
					Levantando quais são os custos variáveis e fixos, bem como qual é o lucro da empresa em um determinado período.
					<br>
					Realizadas entre os dias 16/07 a 12/11 – carga horária de 04h<br>
					Duas visitas com duração de  02h cada, total de 14 empresas do Núcleo Pioneiro receberam as consultorias.
					<br><br>
					<b>Consultorias de Marketing:</b> Buscou proporcionar aos empreendedores um planejamento das ações em marketing, definindo metas e atividades para serem desenvolvidas durante um período determinado de tempo. Definindo o público alvo e o posicionamento de marca.
					<br>Realizadas entre os dias 02/08 a 20/11 – carga horária de 04h<br>
					Duas visitas com duração de  02h cada, total de 14 empresas do Núcleo Pioneiro receberam as consultorias.<br>
					<br>PRINCIPAIS ATIVIDADES REALIZADAS:
						<br>- Identificação e percepção do seu público-alvo, bem como seu diferencial ou os principais aspectos de suas lojas, que geram maior volume de negócios;
					 
						<br>- Orientações de divulgação: seus produtos, suas marcas, suas empresas e suas promoções, utilizando-se principalmente de Carro de som, Panfleto e Recursos de Internet, pois a maiorias de seus clientes são do próprio bairro.
						<br>- Sugeridas várias ações promocionais, com possibilidade de ações mensais, desde que sejam diferentes uma a cada mês, com objetivos, públicos e benefícios diferentes. 
						<br>- Repassadas estratégias e dicas de vitrine, quanto a sua estrutura, formas de exposição, produtos, manequins, decoração e cores. 
						<br>- Abordados a importância do layout interno de sua loja, ou seja, espaços internos, exposição de produtos, organização e ordenação dos produtos, provadores, caixa, recepção., ventilação e iluminação.
					<br><br>
					<b>Workshop:</b> Gestão do Visual de Loja
					<br>Conteúdo programático:
					<br>- O visual das lojas 
					<br>- Conhecendo as vitrines 
					<br>- Técnicas de exposição de produtos 
					<br>-Detalhes para o visual da loja
					<br>Realizado no dia 23/10 – carga horária de 04h
					<br>O Workshop teve 16 participantes, e presença de 10 empresas do Núcleo Pioneiro. 


