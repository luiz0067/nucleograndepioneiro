			Um grupo de empresários de Toledo, no oeste paranaense, trabalha ações em conjunto para fortalecer e valorizar o comércio em bairros que formam a região da Grande Pioneiro. A união dos comerciantes começou em 1995, com a realização de feiras em conjunto, e ganhou força, a partir de 2004, com a formação do Núcleo Multissetorial da Região da Grande Pioneiro.
 <br><br>
A organização de núcleos setoriais é uma das propostas do Projeto Empreender, parceria entre a Federação das Associações Comerciais e Empresariais do Estado do Paraná (Faciap) e o Sebrae/PR, e realizado, na cidade, com o apoio da Associação Comercial e Empresarial de Toledo (ACIT).
 <br><br>
A iniciativa incentiva empresários de micro e pequenas empresas a debaterem, de forma associativa, problemas e soluções mercadológicas, que beneficiem grupos empresariais.
 <br><br>
O Núcleo Multissetorial da Região da Grande Pioneiro reúne pequenas empresas de diversos segmentos, como confecção e calçados, cosméticos e beleza, semi-joias e acessórios, refrigeração, imobiliária, lojas de utilidades, farmácias e comércio alimentício.
 <br><br>
Essa variedade de empresas deu uma nova característica à proposta do Projeto Empreender, já que o núcleo passou a ser multissetorial. Porém, os problemas que afetam a competitividade do comércio, bem como as soluções que podem potencializar a lucratividade das empresas são comuns. 
 <br><br>
Para o coordenador do Núcleo Multissetorial, Adaildo Cassimiro de Oliveira, as ações realizadas com o Empreender contribuem para, muitas vezes, tirar os empresários da zona de conforto.
 <br><br>
“O grupo é formado por pequenas empresas familiares. A realização de feiras é importante, mas não é a única ação efetiva. É importante ter orientações na área de gestão. Porém, não será o consultor que fará pelo empresário, o consultor dá o caminho das pedras. O Empreender foi fundamental para promover, nos empresários, a visão empreendedora”, avalia Adaildo de Oliveira.
 <br><br>
O consultor do Sebrae/PR, Alan Debus, cita que a união dos empresários já foi responsável por conquistas importantes, inclusive estruturais na região da Grande Pioneiro, como obras para melhorar o transporte e reivindicações em torno da segurança.
 <br><br>
“As ações do grupo, que se fortalecem ao serem realizadas em conjunto, também foram fundamentais para diminuir o êxodo de compradores para outras localidades, como o centro de Toledo, ou até mesmo outras cidades”, avalia.  
 <br><br>
Com o Empreender, os empreendimentos que formam o Núcleo Multissetorial da Região da Grande Pioneiro tiveram, em 2012, acesso a uma série de consultorias e treinamentos. Entre os temas abordados, nas orientações, a importância do empresário pensar e agir de maneira empreendedora, com a realização do curso Saber Empreender, que proporcionou uma reflexão sobre quem é o empreendedor e o empreendedor em ação.
 <br><br>
Na área de finanças, as orientações visaram proporcionar aos empresários informações financeiras para a tomada assertiva de decisões, que levem em consideração os custos fixos e variáveis da empresa, bem como orientações para calcular o lucro do empreendimento, em um determinado período.
 <br><br>
Na área de marketing, os consultores auxiliaram os empreendedores a planejar ações de marketing, definindo, por exemplo, metas e atividades que podem impulsionar a competitividade da empresa, como definir público-alvo e o posicionamento da marca.
 <br><br>
Já na área de visual de loja, os empresários participaram de um workshop. Nesse treinamento, os participantes receberam orientações sobre visual de loja, como estruturar vitrines, técnicas de exposição de produtos e quais detalhes são importantes serem observados no visual da loja.
 <br><br>
A consultora do Programa Empreender na ACIT, Katiane Weisheimer, observa que a região da Grande Pioneiro concentra uma população de cerca de 30 mil habitantes e que a atuação em conjunto dos empresários está sendo decisiva para fortalecer o comércio local.
 <br><br>
“Os próprios empresários buscaram apoio para essa questão de como melhorar a gestão empresarial. Para isso, foi realizado um diagnóstico nas empresas e elaborado um planejamento estratégico das ações. Das 22 empresas que formam o Núcleo Multissetorial da Região da Grande Pioneiro, 15 aceitaram o desafio de participar do Empreender”, conta.
 <br><br>
O consultor do Sebrae/PR ainda ressalta que as consultorias nas empresas foram pontuais, o que contribuiu para, efetivamente, corrigir falhas na gestão e aprimorar a administração.
 <br><br>
“Os empresários adotaram o associativismo, mas conservaram a sua competitividade. Ao trabalhar ações em conjunto, eles conseguem discutir problemas comuns. Organizados, os empresários estão conseguindo, inclusive, transformar a realidade desses bairros”, observa Alan Debus.
 <br><br><br>
<b>Sobre o Sebrae/PR</b>
 <br><br>
O Sebrae/PR - Serviço de Apoio às Micro e Pequenas Empresas do Paraná é uma instituição sem fins lucrativos criada para dar apoio aos empresários de micro e pequenas empresas e aos empreendedores interessados em abrir o próprio negócio. No Brasil, são 27 unidades e 800 postos de atendimentos espalhados de norte a sul. No Paraná, cinco regionais e 11 escritórios. A entidade chega aos 399 municípios do Estado por meio de atendimento itinerante, pontos de atendimento e de parceiros como associações, sindicatos, cooperativas, órgãos públicos e privados. O Sebrae/PR oferece palestras, orientações, capacitações, treinamentos, projetos, programas e soluções empresariais, com foco em empreendedorismo, setores estratégicos, políticas públicas, tecnologia e inovação, orientação ao crédito, acesso ao mercado, internacionalização, redes de cooperação e programas de lideranças.
