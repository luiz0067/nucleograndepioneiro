			Núcleo Multissetorial da Região da Grande Pioneiro, 
			formado por pequenas empresas de diversos segmentos, implanta soluções para melhorar competitividade do varejo; 
			em 2012, empresários participaram de consultorias e treinamentos nas áreas de finanças, marketing e visual de loja, 
			além de orientações sobre importância de pensar e agir de maneira empreendedora
